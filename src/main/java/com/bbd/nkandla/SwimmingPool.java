package com.bbd.nkandla;

public class SwimmingPool {
	
	private int length;
	private int width;
	
	public SwimmingPool(int width, int length){
		this.length = length;
		this.width = width;
	}

	public int getLength (){
		return this.length;
	}
	
	public int getWidth (){
		return this.width;
	}
	
	
}
